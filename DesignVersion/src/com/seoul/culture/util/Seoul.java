package com.seoul.culture.util;

public class Seoul {
	public int id;
	public String gu = "";
	public String name = "";
	public String address = "";
	public String nmap = "";
	public String page = "";
	public String number = "";

	public Seoul() {
	}


	public Seoul(int id, String gu, String name, String address, String nmap, String page, String number) {
		super();
		this.id = id;
		this.gu = gu;
		this.name = name;
		this.address = address;
		this.nmap = nmap;
		this.page = page;
		this.number = number;
	}


}
